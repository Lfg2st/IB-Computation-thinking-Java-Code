public class if_else_ladders {
    public static void main(String[] args) {
        int a = 0;
        if (a ==0){
            System.out.println("A does equal 0");
        }
        else if (a ==1){
            System.out.println("A does equal 1");
        }
        else if (a ==2){
            System.out.println("A does equal 2");
        }
        else{
            System.out.println("A doesn't equal 0, 1 or 2");
        }


    }
}
