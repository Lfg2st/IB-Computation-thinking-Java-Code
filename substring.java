public class substring {

        public static void main(String[] args) {
            String str = "Hello, World!";
            
            // Extract substring from index 2 to index 5 (exclusive)
            String substring = str.substring(2, 5);
            
            System.out.println("Substring from index 2 to index 5: " + substring); // Output: "llo"
        }
    
}
